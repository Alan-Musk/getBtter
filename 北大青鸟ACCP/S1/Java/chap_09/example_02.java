package chap_09;

public class example_02 {
	public static void main(String[] args) {
		System.out.println("打印矩形");
		for (int i = 0; i <=5; i++) {
			for (int j = 0; j <=5; j++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}
}
